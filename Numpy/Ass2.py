import numpy as np

#with empty
a = np.empty((3,5),dtype=int)
print(a)

print()
print('------------------------')
print()

#with full
b = np.full([3,5],5,dtype=int)
print(b)

print()
print('------------------------')
print()

#with zeros
c = np.zeros((3,5),dtype=int)
print(c)

print()
print('------------------------')
print()

#with ones
d = np.ones((3,5),dtype=int)
print(d)